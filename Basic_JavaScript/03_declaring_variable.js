/**
 * Variable(변수) 선언하기
 * 
 * 1. var - 실무에서 더 이상 쓰지 않음.(오래전에 만든 코드들은
 * 존재 가능)
 * 2. let - 
 * 3. const -
 */
var code = 'Gyu코드'; // 변수 변수명 = '값';
console.log(code);

let age = 20; // 변수값에 ''을 넣으면 문자 안넣으면 숫자로 인식
console.log(age);

/**
 * let과 var로 선언하면
 * 값을 추후 변경할 수 있다.
 */

/**
 * 선언과 할당
 * 
 * 1. 변수를 선언하는 것
 * 2. var든 let든 변수에 이름을 지정해주는 것.
 */
var name = '이동규'; // <-- 오른쪽의 값을 선언된 name "변수"에 "할당"한다.
console.log(name);